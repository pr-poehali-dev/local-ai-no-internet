            a, b = int(a), int(b)
            
            if op in ['+']:
                result = a + b
            elif op in ['-']:
                result = a - b
            elif op in ['*', '√ó']:
                result = a * b
            elif op in ['/', '√∑']:
                result = a / b if b != 0 else '–¥–µ–ª–µ–Ω–∏–µ –Ω–∞ –Ω–æ–ª—å –Ω–µ–≤–æ–∑–º–æ–∂–Ω–æ'
            
